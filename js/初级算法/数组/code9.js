var isValidSudoku = function(board) {
  let hash=[];
  for(i in border){
    for(j in board){
      if(board[i][j]!=='.'){
        hash.push(board[i][j]);
      }
      let set=new Set(hash);
      if(set.length!==hash.length){
        return false;
      }
      hash=[];
    }
  }


};

let border= [
  ["8","3",".",".","7",".",".",".","."],
  ["6",".",".","1","9","5",".",".","."],
  [".","9","8",".",".",".",".","6","."],
  ["8",".",".",".","6",".",".",".","3"],
  ["4",".",".","8",".","3",".",".","1"],
  ["7",".",".",".","2",".",".",".","6"],
  [".","6",".",".",".",".","2","8","."],
  [".",".",".","4","1","9",".",".","5"],
  [".",".",".",".","8",".",".","7","9"]
];
isValidSudoku(border);
